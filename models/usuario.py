class Usuario:
    def __init__(self, usuario_id, nome, email, cargo, administrador, modalidade, horario_entrada, horario_saida):
        self.usuario_id = usuario_id
        self.nome = nome
        self.email = email
        self.cargo = cargo
        self.administrador = administrador
        self.modalidade = modalidade
        self.horario_entrada = horario_entrada
        self.horario_saida = horario_saida
